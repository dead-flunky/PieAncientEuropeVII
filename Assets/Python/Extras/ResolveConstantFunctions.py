def replace_key_functions(fsource, ftarget):
		f = file(fsource, 'rU')  # noqa
		f2 = file(ftarget, 'w')  # noqa
		l = f.readline()
		while(l):
				# Ignore Lines without identation"
				if l[0] == "\t" or l[:2] == "  ":
						l2 = replaceLoop(l)
				else:
						l2 = l

				f2.write(l2)
				l = f.readline()

		f2.close()
		f.close()


